    private void jButtonSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajActionPerformed
        try {
            int id = 0;
            String oznaka = "";
            double cena = 0;
            int garancija = 0;
            String magneti = "";
            String pragovi = "";
            String telo = "";
            String boja = "";
            if (!jTextFieldIDModela.getText().equals("")) {
                id = Integer.parseInt(jTextFieldIDModela.getText());
            } else {
                JOptionPane.showMessageDialog(this, "Morate uneti neiskorišćen ID");
                return;
            }

            if (!jTextFieldOznaka.getText().equals("")) {
                oznaka = jTextFieldOznaka.getText().trim();
            } else {
                JOptionPane.showMessageDialog(this, "Morate uneti oznaku modela");
                return;
            }

            String status = "nepoznato";
            if (jRadioButtonNovo.isSelected()) {
                status = "Novo";
            } else if (jRadioButtonPolovno.isSelected()) {
                status = "Polovno";
            }

            if (!jTextFieldCena.getText().equals("")) {
                cena = Double.parseDouble(jTextFieldCena.getText().trim());
            } else {
                JOptionPane.showMessageDialog(this, "Morate uneti cenu modela");
                return;
            }
            if (!jTextFieldGarancija.getText().equals("")) {
                garancija = Integer.parseInt(jTextFieldGarancija.getText());
            } else {
                JOptionPane.showMessageDialog(this, "Morate uneti trajanje garancije za model");
                return;
            }

            if (!jTextFieldMagneti.getText().equals("")) {
                magneti = jTextFieldMagneti.getText().trim();
            } else {
                JOptionPane.showMessageDialog(this, "Morate uneti opis magneta");
                return;
            }

            if (!jTextFieldPragovi.getText().equals("")) {
                pragovi = jTextFieldPragovi.getText().trim();
            } else {
                JOptionPane.showMessageDialog(this, "Morate uneti broj pragova");
                return;
            }
            if (!jTextFieldTelo.getText().equals("")) {
                telo = jTextFieldTelo.getText().trim();
            } else {
                JOptionPane.showMessageDialog(this, "Morate uneti opis tela modela");
                return;
            }
            if (!jTextFieldBoja.getText().equals("")) {
                boja = jTextFieldBoja.getText().trim();
            } else {
                JOptionPane.showMessageDialog(this, "Morate uneti boju modela");
                return;
            }

            Proizvodjac p = (Proizvodjac) jComboBoxProizvodjac.getSelectedItem();

            ModelGitare mg = new ModelGitare(id, cena, status, oznaka, garancija, magneti, pragovi, telo, boja, p);

            KlijentTransferObjekat kto = new KlijentTransferObjekat();

            if (jButtonSacuvaj.getText().equals("Sačuvaj")) {
                kto.setOperacija(Konstante.DODAJ_MODEL);
            } else {
                kto.setOperacija(Konstante.IZMENI_MODEL);
            }
            kto.setParametar(mg);
            Komunikacija.getInstance().posaljiZahtev(kto);
            ServerTransferObjekat sto = Komunikacija.getInstance().procitajOdgovor();
            if (sto.getRezultat() == Konstante.REZULTAT_NOK) {
                throw new Exception(sto.getIzuzetak());
            }
            JOptionPane.showMessageDialog(this, "Model je uspešno sačuvan!");
            SwingUtilities.getWindowAncestor(this).setVisible(false);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Model nije sačuvan!");
            ex.printStackTrace();
        }

    }//GEN-LAST:event_jButtonSacuvajActionPerformed


