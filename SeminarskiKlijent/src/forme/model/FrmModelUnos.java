    private void srediFormu() {
        try {
            KlijentTransferObjekat kto = new KlijentTransferObjekat();
            kto.setOperacija(Konstante.VRATI_PROIZVODJACE);
            Komunikacija.getInstance().posaljiZahtev(kto);
            ServerTransferObjekat sto = Komunikacija.getInstance().procitajOdgovor();
            List<OpstiDomenskiObjekat> lpr = (List<OpstiDomenskiObjekat>) sto.getOdgovor();
            jComboBoxProizvodjac.removeAllItems();
            for (OpstiDomenskiObjekat pr : lpr) {
                jComboBoxProizvodjac.addItem((Proizvodjac) pr);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

    private void prikazModela(ModelGitare mg) {
        jTextFieldIDModela.setText(mg.getModelGitareID() + "");
        jTextFieldCena.setText(mg.getCenaModela() + "");
        jTextFieldPragovi.setText(mg.getPragovi());
        jTextFieldGarancija.setText(mg.getGarancija() + "");
        jTextFieldTelo.setText(mg.getTelo());
        jTextFieldBoja.setText(mg.getBoja());
        jTextFieldMagneti.setText(mg.getMagneti());
        jTextFieldOznaka.setText(mg.getOznakaModela());
        jComboBoxProizvodjac.setSelectedItem(mg.getProizvodjac());
        if (mg.getStatusModela().equals("Stariji")) {
            jRadioButtonPolovno.setSelected(true);
        } else {
            jRadioButtonNovo.setSelected(true);
        }
    }

